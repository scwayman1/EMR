/**
 * EMR-110 — AI Patient Education Sheets
 *
 * Generates personalized, third-grade-reading-level handouts that the
 * patient gets after a visit. Each sheet is built from:
 *
 *   • the diagnosis (ICD-10) — provides the structural template,
 *   • the dispensary product or medication — fills in dose, timing,
 *     side-effect copy,
 *   • the patient's age band — picks pictograms, vocabulary level, and
 *     guardian-facing addenda where appropriate,
 *   • the patient's preferred language — EN / ES (more added per
 *     population needs).
 *
 * The model client is *optional*. When it's available the sheet uses an
 * LLM to produce a warm, fairytale-style summary. When it isn't (CI,
 * cost guardrails) it falls back to a deterministic template. The shape
 * of the result is identical either way so callers don't branch.
 *
 * Each sheet is auditable: the inputs, model used (if any), and the
 * generated text are persisted to the patient record so a clinician can
 * later see exactly what the patient took home.
 *
 * Dr. Patel directive: every sheet ends with the patient outcome-tracking
 * checklist (emoji-first sentiment + 1-10 scale) so research data continues
 * to flow from the at-home setting.
 */

import type { ModelClient } from "@/lib/orchestration/types";
import type { AgeBand } from "@/lib/utils/patient-age";

// ---------------------------------------------------------------------------
// Inputs / outputs
// ---------------------------------------------------------------------------

export interface PatientContextForEducation {
  firstName: string;
  preferredLanguage?: "en" | "es";
  ageBand: AgeBand;
  age: number | null;
  /** True when the patient is a minor — adds a guardian note. */
  hasGuardian?: boolean;
  guardianName?: string | null;
  /** Important things to call out (allergies, advanced directives, etc.). */
  flags?: string[];
}

export interface EducationSheetTopic {
  /** ICD-10 the sheet is built around. */
  icd10: string;
  diagnosisLabel: string;
  /** Friendly, plain-language title. */
  title: string;
  /** Optional medication / cannabis product the sheet is about. */
  product?: {
    name: string;
    formulation?: string;
    cannabinoids?: string[];
    dose?: string;
    schedule?: string;
  };
  /** Visit-specific context the clinician wants the patient to remember. */
  visitNotes?: string;
}

export interface EducationSheetSection {
  id: string;
  emoji: string;
  heading: string;
  body: string;
  /** Bulleted action items the patient should take this week. */
  actions?: string[];
}

export interface EducationSheet {
  id: string;
  generatedAt: string;
  language: "en" | "es";
  topic: EducationSheetTopic;
  sections: EducationSheetSection[];
  /** Patient outcome-tracking checklist — emoji + 1-10 prompts. */
  outcomeChecklist: OutcomePrompt[];
  /** Stub references the clinician can verify (Cochrane / NIH). */
  references: string[];
  /** Whether the body was generated by a model or template. */
  source: "llm" | "template";
  modelLabel?: string;
}

export interface OutcomePrompt {
  id: string;
  /** "How was your sleep?" / "Pain level today?" etc. */
  question: string;
  /** Three faces the patient taps. */
  faces: ["😞", "😐", "😀"];
  /** 1-10 scale anchors. */
  scale: { min: string; max: string };
}

