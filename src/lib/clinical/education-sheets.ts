export interface GenerateEducationSheetOptions {
  topic: EducationSheetTopic;
  patient: PatientContextForEducation;
  /** Optional model client — when present, LLM mode is used. */
  model?: ModelClient;
}

export async function generateEducationSheet({
  topic,
  patient,
  model,
}: GenerateEducationSheetOptions): Promise<EducationSheet> {
  const language = patient.preferredLanguage ?? "en";
  const blueprint =
    SECTION_BLUEPRINT_BY_ICD[topic.icd10.toUpperCase()] ?? DEFAULT_BLUEPRINT;

  let sections: EducationSheetSection[];
  let source: "llm" | "template" = "template";
  let modelLabel: string | undefined;

  if (model) {
    try {
      const built = await llmBody(blueprint, topic, patient, model);
      sections = built.sections;
      source = built.source;
      modelLabel = built.modelLabel;
    } catch {
      sections = blueprint.map((b) => {
        const t = templateBody(b.sectionId, topic, patient);
        return {
          id: b.sectionId,
          emoji: b.emoji,
          heading: b.heading,
          body: t.body,
          actions: t.actions,
        };
      });
    }
  } else {
    sections = blueprint.map((b) => {
      const t = templateBody(b.sectionId, topic, patient);
      return {
        id: b.sectionId,
        emoji: b.emoji,
        heading: b.heading,
        body: t.body,
        actions: t.actions,
      };
    });
  }

  // Guardian addendum (minor)
  if (patient.hasGuardian) {
    sections.push({
      id: "guardian",
      emoji: "🪪",
      heading: "Note for guardian",
      body:
        `${patient.guardianName ?? "Guardian"}, please review this plan with ${patient.firstName} ` +
        `and call us if anything is unclear before the next dose.`,
    });
  }

  // Flag addendum (allergies, etc.)
  if (patient.flags && patient.flags.length > 0) {
    sections.push({
      id: "flags",
      emoji: "⚠️",
      heading: "Important reminders",
      body: patient.flags.map((f) => `• ${f}`).join("\n"),
    });
  }

  return {
    id: `edu-${topic.icd10}-${Date.now()}`,
    generatedAt: new Date().toISOString(),
    language,
    topic,
    sections,
    outcomeChecklist: outcomeChecklistForIcd(topic.icd10),
    references: referencesForIcd(topic.icd10),
    source,
    modelLabel,
  };
}

function referencesForIcd(icd: string): string[] {
  const code = icd.toUpperCase();
  if (code.startsWith("G89") || code === "M79.7") {
    return [
      "Whiting et al., 2015. JAMA. Cannabinoids for chronic pain.",
      "National Academies of Sciences, 2017. The health effects of cannabis.",
    ];
  }
  if (code === "G47.00") {
    return [
      "Babson et al., 2017. Cannabis, sleep, and dreaming.",
      "Suraev et al., 2020. Cannabinoid therapies for sleep — systematic review.",
    ];
  }
  if (code.startsWith("F41") || code.startsWith("F40")) {
    return [
      "Bergamaschi et al., 2011. CBD for social anxiety disorder.",
      "Skelley et al., 2020. CBD for anxiety — review.",
    ];
  }
  if (code === "F43.10") {
    return [
      "Bonn-Miller et al., 2021. Cannabis and PTSD — RCT.",
    ];
  }
  return ["See your clinician's notes for sources used in this sheet."];
}

// ---------------------------------------------------------------------------
// Helpers for the surrounding chart UI
// ---------------------------------------------------------------------------

export function plainTextSheet(sheet: EducationSheet): string {
  const lines: string[] = [];
  lines.push(`📄 ${sheet.topic.title}`);
  lines.push(`For: ${sheet.topic.diagnosisLabel}`);
  lines.push("");
  for (const s of sheet.sections) {
    lines.push(`${s.emoji} ${s.heading}`);
    lines.push(s.body);
    if (s.actions && s.actions.length > 0) {
      lines.push("");
      lines.push(s.actions.map((a) => `  • ${a}`).join("\n"));
    }
    lines.push("");
  }
  if (sheet.outcomeChecklist.length > 0) {
    lines.push("📊 How to track how you're doing");
    for (const p of sheet.outcomeChecklist) {
      lines.push(`  ${p.faces.join(" / ")}  ${p.question}`);
      lines.push(`     scale: ${p.scale.min}  →  ${p.scale.max}`);
    }
    lines.push("");
  }
  if (sheet.references.length > 0) {
    lines.push("📚 References");
    for (const r of sheet.references) lines.push(`  • ${r}`);
  }
  return lines.join("\n");
}
