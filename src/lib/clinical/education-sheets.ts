function outcomeChecklistForIcd(icd: string): OutcomePrompt[] {
  const code = icd.toUpperCase();
  if (code.startsWith("G89") || code.startsWith("M54") || code === "M79.7" || code.startsWith("G43")) {
    return [OUTCOME_PROMPT_LIBRARY.pain, OUTCOME_PROMPT_LIBRARY.side_effects];
  }
  if (code.startsWith("G47")) {
    return [OUTCOME_PROMPT_LIBRARY.sleep, OUTCOME_PROMPT_LIBRARY.side_effects];
  }
  if (
    code.startsWith("F41") ||
    code.startsWith("F40") ||
    code.startsWith("F43")
  ) {
    return [
      OUTCOME_PROMPT_LIBRARY.anxiety,
      OUTCOME_PROMPT_LIBRARY.mood,
      OUTCOME_PROMPT_LIBRARY.sleep,
      OUTCOME_PROMPT_LIBRARY.side_effects,
    ];
  }
  if (code.startsWith("F32") || code.startsWith("F33")) {
    return [
      OUTCOME_PROMPT_LIBRARY.mood,
      OUTCOME_PROMPT_LIBRARY.sleep,
      OUTCOME_PROMPT_LIBRARY.side_effects,
    ];
  }
  return [OUTCOME_PROMPT_LIBRARY.mood, OUTCOME_PROMPT_LIBRARY.side_effects];
}

// ---------------------------------------------------------------------------
// Template-mode body
// ---------------------------------------------------------------------------

function templateBody(
  sectionId: string,
  topic: EducationSheetTopic,
  patient: PatientContextForEducation,
): { body: string; actions?: string[] } {
  const product = topic.product?.name ?? "your medicine";
  const dose = topic.product?.dose ?? "the dose your doctor prescribed";
  const schedule = topic.product?.schedule ?? "the times your doctor wrote down";
  const name = patient.firstName;

  switch (sectionId) {
    case "what":
      return {
        body:
          `Hey ${name} — your visit today was about ${topic.diagnosisLabel.toLowerCase()}. ` +
          `That just means ${plainLanguageForDiagnosis(topic.icd10)}.`,
      };
    case "why_cannabis":
      return {
        body:
          `Cannabis works through your body's own endocannabinoid system. For ${topic.diagnosisLabel.toLowerCase()}, ` +
          `the goal is to take the edge off without making you feel out of it. We picked ${product} because it fits that goal.`,
      };
    case "how_to_use":
      return {
        body:
          `Take ${dose}, ${schedule}. ` +
          `Start at the lower end if you've never tried it before — small wins build trust with this medicine.`,
        actions: [
          `Take your first dose at home, in a calm spot.`,
          `Set a phone reminder for tomorrow to check in on how you feel.`,
          `Keep ${product} in a place that's safe from kids and pets.`,
        ],
      };
    case "watch_for":
      return {
        body:
          `Most people feel fine. But call us right away if you feel: ` +
          `racing heart, trouble breathing, very confused, very anxious, or you can't keep food down. ` +
          `For real emergencies (like chest pain) — call 911.`,
      };
    case "lifestyle":
      return {
        body:
          `A 10-minute walk + good sleep amplifies everything we're doing. ` +
          `Drink water, take rest seriously, and don't be afraid to message us with questions.`,
      };
    case "calm_tools":
      return {
        body:
          `Try the 4-7-8 breath: in for 4, hold for 7, out for 8. ` +
          `It sounds simple, but it works because your body trusts your breath.`,
      };
    case "grounding":
      return {
        body:
          `When things feel overwhelming, try the 5-4-3-2-1 trick: name 5 things you see, ` +
          `4 things you can touch, 3 things you hear, 2 things you smell, and 1 thing you taste.`,
      };
    case "questions":
      return {
        body:
          `Write down the questions that pop into your head — even if they feel small. ` +
          `Bring the list to your next visit.`,
      };
    case "plan":
      return {
        body: `Take ${dose} ${schedule}. ` + (topic.visitNotes ?? ""),
      };
    default:
      return { body: "" };
  }
}

function plainLanguageForDiagnosis(icd10: string): string {
  const code = icd10.toUpperCase();
  if (code.startsWith("G89")) return "you've been having pain that just won't quit";
  if (code === "G47.00") return "your brain isn't giving you the rest you need";
  if (code === "F41.1") return "your brain has been on a high-alert setting too often";
  if (code === "F43.10") return "your nervous system is still on guard from something hard";
  if (code === "F32.9" || code.startsWith("F33"))
    return "your mood has been low for a while and we're going to help lift it";
  if (code === "M79.7")
    return "your body sends pain signals more loudly than other folks";
  if (code === "G35") return "your nerves' insulation needs some help";
  if (code.startsWith("G40")) return "your brain has unexpected electrical bursts";
  return "the thing we talked about today";
}

// ---------------------------------------------------------------------------
// LLM-mode body
// ---------------------------------------------------------------------------

async function llmBody(
  blueprint: { sectionId: string; emoji: string; heading: string }[],
  topic: EducationSheetTopic,
  patient: PatientContextForEducation,
  model: ModelClient,
): Promise<{ source: "llm"; sections: EducationSheetSection[]; modelLabel: string }> {
  // Build a single prompt that returns a JSON array of sections so we
  // only spend one LLM call per sheet.
  const prompt = buildPrompt(blueprint, topic, patient);
  const raw = await model.complete(prompt, { temperature: 0.4, maxTokens: 900 });
  let parsed: Array<{ sectionId: string; body: string; actions?: string[] }> = [];
  try {
    const start = raw.indexOf("[");
    const end = raw.lastIndexOf("]");
    if (start >= 0 && end > start) {
      parsed = JSON.parse(raw.slice(start, end + 1));
    }
  } catch {
    parsed = [];
  }
  const byId = new Map(parsed.map((p) => [p.sectionId, p]));
  const sections: EducationSheetSection[] = blueprint.map((b) => {
    const llm = byId.get(b.sectionId);
    if (llm && typeof llm.body === "string" && llm.body.length > 0) {
      return {
        id: b.sectionId,
        emoji: b.emoji,
        heading: b.heading,
        body: llm.body,
        actions: llm.actions,
      };
    }
    const fallback = templateBody(b.sectionId, topic, patient);
    return {
      id: b.sectionId,
      emoji: b.emoji,
      heading: b.heading,
      body: fallback.body,
      actions: fallback.actions,
    };
  });
  return { source: "llm", sections, modelLabel: "configured-model" };
}

function buildPrompt(
  blueprint: { sectionId: string; emoji: string; heading: string }[],
  topic: EducationSheetTopic,
  patient: PatientContextForEducation,
): string {
  const product = topic.product
    ? `${topic.product.name}${topic.product.formulation ? ` (${topic.product.formulation})` : ""}` +
      `${topic.product.dose ? `, dose ${topic.product.dose}` : ""}` +
      `${topic.product.schedule ? `, schedule ${topic.product.schedule}` : ""}`
    : "no specific product";
  const guardianAddendum = patient.hasGuardian
    ? `Patient is a minor; guardian is ${patient.guardianName ?? "on file"}. Address sections to the guardian where appropriate.\n`
    : "";
  return [
    `You are writing a patient education sheet for ${patient.firstName}, age ${patient.age ?? "unknown"} (${patient.ageBand}).`,
    `Diagnosis: ${topic.diagnosisLabel} (${topic.icd10}).`,
    `Product/medicine: ${product}.`,
    `Visit notes from clinician: ${topic.visitNotes ?? "none"}.`,
    guardianAddendum,
    `Tone: warm, third-grade reading level, no medical jargon.`,
    `Language: ${patient.preferredLanguage === "es" ? "Spanish" : "English"}.`,
    `Output strictly a JSON array. Each item is { "sectionId": string, "body": string, "actions"?: string[] }.`,
    `Use these sectionIds in this order: ${blueprint.map((b) => b.sectionId).join(", ")}.`,
    `Do not include the headings or emojis — those are added by the renderer.`,
    `Each body should be 2–4 short sentences.`,
  ].join("\n");
}

